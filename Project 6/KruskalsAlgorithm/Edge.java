
package KruskalsAlgorithm;

public class Edge {
	private int source;
	private int destn;
	private int distance;
	/**
	 * Getter method for U
	 * @return
	 */
	public int getSource() {
		return source;
	}
	/**
	 * Setter method for U
	 * @param u
	 */
	public void setSource(int source) {
		this.source = source;
	}
	/**
	 * Getter method for V
	 * @return
	 */
	public int getDestn() {
		return destn;
	}
	/**
	 * Setter Method for V
	 * @param v
	 */
	public void setDestn(int destn) {
		this.destn = destn;
	}
	/**
	 * Getter method for W
	 * @return
	 */
	public int getDistance() {
		return distance;
	}
	/**
	 * Setter Method for W
	 * @param w
	 */
	public void setDistance(int distance) {
		this.distance = distance;
	}
	

}
